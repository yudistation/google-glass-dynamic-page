        <h2>O que é</h2>
        <p>O <span style="font-weight:bolder;">Google Glass</span> é um acessório em forma de óculos que possibilita a in&shy;te&shy;ra&shy;ção dos u&shy;su&shy;á&shy;ri&shy;os com diversos con&shy;te&shy;ú&shy;dos em realidade aumentada. Também
            chamado de <a href="https://google.com/glass/start/" target="_blank">Project
                Glass</a>, o eletrônico é capaz de tirar fotos a partir de comandos de voz, enviar mensagens instantâneas e realizar vídeo&shy;con&shy;fe&shy;rên&shy;ci&shy;as. Seu lançamento está previsto para 2014, e seu preço deve ser de US$
            1,5 mil. Atualmente o <em>Google Glass</em> encontra-se em fase de testes e já possui um vídeo totalmente gravado com o dispositivo. Além disso, a companhia de buscas registrou novas patentes anti-furto e de desbloqueio de tela para
            o acessório.</p>

